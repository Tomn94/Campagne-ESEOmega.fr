<p>
  Tous les ans, l’ESEO vit une semaine très intense pour l’élection du nouveau BDE. Le Bureau Des Étudiants va gérer la vie associative pendant un an. Entre rires et professionnalisme, le nouveau BDE devra mériter ses voix pour devenir le nouveau Bureau de l’ESEO.
</p>
<p>
  Une semaine de campagne est traditionnellement organisée pour montrer aux élèves sa motivation ainsi que ses capacités à assumer ce nouveau rôle. Deux listes vont s’affronter au cours de la semaine pour prétendre à ces responsabilités.
</p>
<p>
  Le BDE ESEOmega sera présent toute la semaine pour vous servir afin de vous faire passer une semaine inoubliable. Entre activités, défis, repas, évènements, soirées, nous vous attendons nombreux et sommes prêts à vous accueillir pour vous faire passer une semaine de folie !
</p>
